public class datatypes{
    public static void main(String[] args){
       byte a=12;
        System.out.println(a);
       
         int b=34;
        System.out.println(b);
        short sh=13;
        System.out.println(sh);
        boolean bl=true;
        System.out.println(bl);
        double c=0.5;
        System.out.println(c);
        float d=44.4F;
        long l=45678932;
       System.out.println(l);
    }
}    